public class Propagate {
	static String reverse(String str) {
		try { 
			str.length();
		}
		catch(Exception e)
		{
			e.printStackTrace();
		}
		finally{
			System.out.println("Ended the Process");
		}

		String revstr = "";
		for(int j=str.length()-1;j>=0;--j) {
			revstr +=str.charAt(j);
		}
		return revstr;
	
	}
	
	public static void main(String [] args) {
		System.out.println(reverse(""));
	
	
	}

}
